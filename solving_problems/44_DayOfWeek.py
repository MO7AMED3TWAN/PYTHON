while True :
    NumberOfDay=int(input("Enter The NUmber Of Day : "))
    if NumberOfDay == 1 :
        print("It's Sunday !!")
        break
    elif NumberOfDay == 2 :
        print("It's Monday !!")
        break
    elif NumberOfDay == 3 :
        print("It's Tuesday !!")
        break
    elif NumberOfDay == 4 :
        print("It's Wednesday !!")
        break
    elif NumberOfDay == 5:
        print("It's Thursday !!")
        break
    elif NumberOfDay == 6:
        print("It's Friday !!")
        break
    elif NumberOfDay == 7:
        print("It's Saturday !!")
        break
    else:
        print("Wrong Day ,, Please Enter The Number Again ")

